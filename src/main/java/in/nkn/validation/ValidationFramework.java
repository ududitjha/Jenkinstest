    /**
     * Utility method to validated a field if it is empty.
     *
     * @param stringToValidate The string which is being validated.
     * @param name Name of the parameter
     * @return SUCCESS if validated else error message.
     *
     */
    public String isEmpty(String stringToValidate, String name) {
        String message = "Please Enter " + name + ".";
        if (!((stringToValidate.length() <= 0) || (stringToValidate.equals("")) || (stringToValidate == null))) {
            message = "SUCCESS";
        }
        return message;
    }

